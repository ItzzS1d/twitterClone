userSchema.pre("findOneAndUpdate", async function (next) {
  if (this.getUpdate().password) {
    this.getUpdate().password = await bcrypt.hash(
      this.getUpdate().password,
      10
    );
  }
  next();
});

const User = mongoose.model("User", userSchema);

export default User;
